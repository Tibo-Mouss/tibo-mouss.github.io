function init() {
    getEnglishTrad();

    // Change language if the lang url parameter has been set
    if (trad_url_param) {
        if (Object.keys(translations).includes(trad_url_param)) {
            translate(trad_url_param);
        }
    }
}

function getEnglishTrad() {
    for (let i = 0; i < translationLinks.length; i++) {
        translations['EN'][translationLinks[i].getAttribute("trad")] = translationLinks[i].innerHTML;
    }
}


function translate(language) {
    for (let i = 0; i < translationLinks.length; i++) {
        translationLinks[i].innerHTML = translations[language][translationLinks[i].getAttribute("trad")];
    }
}


